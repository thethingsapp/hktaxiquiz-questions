def main():
    # 載入現有數據
    with open('all_locations_improved.json', 'r', encoding='utf-8') as f:
        locations = json.load(f, object_pairs_hook=OrderedDict)
    
    print(f"📖 當前數據: {len(locations)} 個地點")
    print(f"🎯 官方地點: {len(OFFICIAL_LOCATIONS)} 個 (86-319)")
    print(f"\n開始補齊...")
    print("="*70)
    
    added = 0
    skipped = 0
    
    for place, location in OFFICIAL_LOCATIONS.items():
        if place not in locations:
            locations[place] = location
            added += 1
            print(f"✅ 添加: {place:40s} → {location}")
        else:
            skipped += 1
            if locations[place] != location:
                print(f"⚠️  修正: {place:40s} 官方:{location} | 現有:{locations[place]}")
                locations[place] = location
    
    # 保存
    with open('all_locations_improved.json', 'w', encoding='utf-8') as f:
        json.dump(locations, f, ensure_ascii=False, indent=2)
    
    print("\n" + "="*70)
    print(f"📊 統計:")
    print(f"   ✅ 新增: {added} 個")
    print(f"   ⏭️  已有: {skipped} 個")
    print(f"   📁 總計: {len(locations)} 個")
    print(f"   💾 已保存到 all_locations_improved.json")
    
    return locations

